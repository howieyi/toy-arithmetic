/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {TreeNode}
 */
var convertBST = function (root) {
  var sum = 0;
  var dfs = function (root) {
    if (root != null) {
      dfs(root.right);
      sum += root.val;
      console.log(sum);
      root.val = sum;
      dfs(root.left);
    }
  }

  dfs(root);
  return root;
};


